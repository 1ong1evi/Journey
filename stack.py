class Node:
    def __init__(self, value, next_node=None):
        self.value = value
        self.next_node = next_node

    def get_value(self):
        return self.value

    def get_next_node(self):
        return self.next_node

    def set_next_node(self, next_node):
        self.next_node = next_node


class Stack:
    def __init__(self, limit=10000):
        self.top_item = None
        self.size = 0
        self.limit = limit
